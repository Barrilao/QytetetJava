    private void ActivarGestionInmobiliaria(){
        if (modeloQytetet.getJugadorActual().tengoPropiedades() && !modeloQytetet.getJugadorActual().isEncarcelado()) {
            this.jbGestion.setEnabled(true);
        } else {
            this.jbGestion.setEnabled(false);
        }
    }
    
    public void habilitarComenzarTurno(){
        this.jbComprar.setEnabled(false);
        this.jbPasarTurno.setEnabled(false);
        this.jbAplicarSorpresa.setEnabled(false);
        this.jbPagarCarcel.setEnabled(false);
        this.jbTirarDado.setEnabled(false);
        this.jbGestion.setEnabled(false);
        if(modeloQytetet.getJugadorActual().isEncarcelado()){
            this.jbPagarCarcel.setEnabled(true);
            this.jbTirarDado.setEnabled(true);
        }
        else
            this.jbJugar.setEnabled(true);
    } 
    
    
    private void jbComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbComprarActionPerformed
        int saldo = modeloQytetet.getJugadorActual().getSaldo();
        int opcion = JOptionPane.showConfirmDialog(null,  "Hola buen señor/a ¿Desea comprar este maravilloso terreno?\nSaldo: " + saldo, "Comprar", JOptionPane.YES_NO_OPTION);
        
        if (opcion == JOptionPane.YES_OPTION) {
            boolean comprado = modeloQytetet.comprarTituloPropiedad();
            if (comprado) {
                this.vistaQytetet1.Actualizar(modeloQytetet);
                JOptionPane.showMessageDialog(this, "Felicidades, has obtenido la propiedad");                
            }
        }
        
        this.jbComprar.setEnabled(false);
        this.jbPasarTurno.setEnabled(true);
        
        ActivarGestionInmobiliaria();
    }//GEN-LAST:event_jbComprarActionPerformed

    private void jbAplicarSorpresaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAplicarSorpresaActionPerformed
        JOptionPane.showMessageDialog(this, modeloQytetet.getTextoCarta());
        int saldoActual = modeloQytetet.getJugadorActual().getSaldo();        
        boolean encarceladoActual = modeloQytetet.getJugadorActual().isEncarcelado();
        boolean activarGestion = true, activarSiguienteJugador = true;
        
        boolean res = modeloQytetet.aplicarSorpresa();        
        
        this.vistaQytetet1.Actualizar(modeloQytetet);
        
        if (saldoActual != modeloQytetet.getJugadorActual().getSaldo()) {
            JOptionPane.showMessageDialog(this, "Tienes cambios en tu saldo: " + (modeloQytetet.getJugadorActual().getSaldo() - saldoActual));
        }
        
        if (encarceladoActual != modeloQytetet.getJugadorActual().isEncarcelado()) {
            JOptionPane.showMessageDialog(this, "No se si eres consciente de que estás en una mazmorra ");
        }
        
        if (modeloQytetet.getJugadorActual().getCasillaActual().getTipo() == TipoCasilla.CALLE) {

            this.jbComprar.setEnabled(!res);
            activarGestion = activarSiguienteJugador = false;
        }
        
        this.jbAplicarSorpresa.setEnabled(false);
        this.jbPasarTurno.setEnabled(activarSiguienteJugador);
        
        if (activarGestion) {
            ActivarGestionInmobiliaria();
        }    
    }//GEN-LAST:event_jbAplicarSorpresaActionPerformed

