    private void jbJugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbJugarActionPerformed
         boolean activarSorpresa = false, activarSiguienteJugador = false, activarComprar = false, activarGestion = false;
        int saldoActual = modeloQytetet.getJugadorActual().getSaldo();
        boolean encarceladoActual = modeloQytetet.getJugadorActual().isEncarcelado();
        
        boolean tienePropietario = modeloQytetet.jugar();
        
        this.vistaQytetet1.Actualizar(modeloQytetet); 
        if (modeloQytetet.getJugadorActual().getSaldo() > 0) {
            if (!modeloQytetet.getJugadorActual().isEncarcelado()) {
                if (modeloQytetet.getJugadorActual().getCasillaActual().getTipo() == qytetetjava.TipoCasilla.CALLE) {
                    if (!tienePropietario) {
                        activarComprar = true;                        
                    } else {
                        String name = ((qytetetjava.Calle)modeloQytetet.getJugadorActual().getCasillaActual()).getTitulo().getPropietario().getNombre();
                        
                        if (name != modeloQytetet.getJugadorActual().getNombre()) {
                            JOptionPane.showMessageDialog(this, "Has caido en la casilla de " + name);
                        }
                        
                        activarSiguienteJugador = true;
                        activarGestion = true;
                    }
                } else if (modeloQytetet.getJugadorActual().getCasillaActual().getTipo() == qytetetjava.TipoCasilla.SORPRESA) {

                    JOptionPane.showMessageDialog(this, "Has caido en una casilla sorpresa");
                    activarSorpresa = true;
                } else if (modeloQytetet.getJugadorActual().getCasillaActual().getTipo() == qytetetjava.TipoCasilla.IMPUESTO){
                    JOptionPane.showMessageDialog(this, "Has caido en una casilla de tipo Impuesto");
                    activarSiguienteJugador = true;   
                    activarGestion = true;
                } else {
                    activarSiguienteJugador = true;
                    activarSorpresa = false;
                    activarGestion = true;
                }
                
            } else {
                JOptionPane.showMessageDialog(this, "A LAS MAZMORRAS DE VENTORMENTA");
                activarSiguienteJugador = true;
            }
            
        } else {
            this.jbComprar.setEnabled(false);
            this.jbPasarTurno.setEnabled(false);
            this.jbAplicarSorpresa.setEnabled(false);
            this.jbPagarCarcel.setEnabled(false);
            this.jbTirarDado.setEnabled(false);
            this.jbJugar.setEnabled(false);   
            this.jbGestion.setEnabled(false);
            JOptionPane.showMessageDialog(this, modeloQytetet.obtenerRanking().toString());
        }
        
        if (saldoActual != modeloQytetet.getJugadorActual().getSaldo()) {
            JOptionPane.showMessageDialog(this, "Tienes cambios en tu saldo: " + (modeloQytetet.getJugadorActual().getSaldo() - saldoActual));
        }
        
        if (encarceladoActual != modeloQytetet.getJugadorActual().isEncarcelado()) {
            JOptionPane.showMessageDialog(this, "No se si eres consciente de que estás en una mazmorra ");
        }
        this.jbJugar.setEnabled(false);
        this.jbPasarTurno.setEnabled(activarSiguienteJugador);
        this.jbAplicarSorpresa.setEnabled(activarSorpresa);
        this.jbComprar.setEnabled(activarComprar);
        if (activarGestion) {
            ActivarGestionInmobiliaria();
        }
    }//GEN-LAST:event_jbJugarActionPerformed

    private void jbGestionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGestionActionPerformed
        Object[] opciones = {"Edificar Casa", "Edificar Hotel", "Vender Propiedad", "Hipotecar Propiedad", "Cancelar Hipoteca"};
        
        int choose = JOptionPane.showOptionDialog(null, "Soldado, que quieres hacer?", "Gestión Inmobiliaria", JOptionPane.DEFAULT_OPTION, 
                JOptionPane.INFORMATION_MESSAGE, null, opciones, opciones[0]);
        
        boolean salir = false;
        
        ArrayList<TituloPropiedad> propiedades = modeloQytetet.getJugadorActual().getPropiedades();
        ArrayList<String> nombresPropiedades = new ArrayList();
        
        String[] propiedadesMenu = new String[propiedades.size()];
        
        for (TituloPropiedad propiedad: propiedades) {
            nombresPropiedades.add(propiedad.getNombre());
        }
        
        propiedadesMenu = nombresPropiedades.toArray(propiedadesMenu);
        
        int eleccion = JOptionPane.showOptionDialog(null, "Selecciona tu choza:", "Propiedades", JOptionPane.DEFAULT_OPTION, 
                JOptionPane.INFORMATION_MESSAGE, null, propiedadesMenu, propiedadesMenu[0]);
        
        Casilla casilla = propiedades.get(eleccion).getCasilla();
        
        if (choose == 0) {
            salir = modeloQytetet.edificarCasa(casilla);
            
            if (!salir) {
                JOptionPane.showMessageDialog(this, "No dispones de suficiente oro o has llegado al limite de casas maldito orco");
            }
        } else if (choose == 1) {
            salir = modeloQytetet.edificarHotel(casilla);
            
            if (!salir) {
                JOptionPane.showMessageDialog(this, "No dispones de suficiente oro o has llegado al limite de hoteles o no dispones de suficientes casas maldito humano");
            }
        } else if (choose == 2) {
            salir = modeloQytetet.venderPropiedad(casilla);
            
            if (!salir) {
                JOptionPane.showMessageDialog(this, "No puedes vender esta propiedad por mucho que insistas");
            }
        } else if (choose == 3) {
            salir = modeloQytetet.hipotecarPropiedad(casilla);
            
            if (!salir) {
                JOptionPane.showMessageDialog(this, "Esta propiedad no es hipotecable");
            }
        } else if (choose == 4) {
            salir = modeloQytetet.cancelarHipoteca(casilla);
            
            if (!salir) {
                JOptionPane.showMessageDialog(this, "No puedes cancelar la hipoteca");
            }
        }

        this.vistaQytetet1.Actualizar(modeloQytetet);
        
        ActivarGestionInmobiliaria();
    
    
    }//GEN-LAST:event_jbGestionActionPerformed

